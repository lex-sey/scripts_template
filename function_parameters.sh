function par1 {
read -p "Введи Параметр1 " val1

if [ $# -eq 0 ]
then
echo "Нет параметров функции "
elif [ $# -gt 2 ]
then
echo "Параметров больше двух"
fi

echo "Имя функции " $0
echo "Параметр функции 1 " $1
echo "Параметр функции 2 " $2
echo "Ввели параметр1 " $val1
echo "Кол-во параметров " $#
}

par1 $1 $2 $3 $4 $5

echo "Параметр скрипта 1 " $1
echo "Параметр скрипта 2 " $2